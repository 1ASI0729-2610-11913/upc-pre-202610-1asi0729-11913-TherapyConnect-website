function setLanguage(lang) {
  const selected = translations[lang] || translations.es;
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (selected[key]) el.textContent = selected[key];
  });
  if (langSwitchTop) langSwitchTop.value = lang;
}

if (langSwitchTop) langSwitchTop.addEventListener('change', (e) => setLanguage(e.target.value));

setLanguage('es');
